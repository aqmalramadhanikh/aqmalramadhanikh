#tipe data>digunakan untuk merepresentasikan nilai suatu data(sesuai dengan pemrograman yang digunakan)
#beberapa tipe data yang kita ketahui

#integer
umur = 19
print("umur saya adalah:",umur)
print(type(umur))


#string
nama = "budi"
print("nama saya:",nama)
print(type(nama))


#float
tinggi = 1.67
print("tinggi saya:",tinggi)
print(type(tinggi))


#boolean
mahasiswa = False
